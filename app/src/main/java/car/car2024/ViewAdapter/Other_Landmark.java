package car.car2024.ViewAdapter;

public class Other_Landmark {

    private String name;

    private int imageId;

    public Other_Landmark(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}